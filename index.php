            document.addEventListener('DOMContentLoaded', function () {
                const nextButton = document.querySelector('.custom-css .owl-next');
                const partnerSection = document.getElementById('partner');
                let autoClickInterval;

                // Function to start the automatic clicking
                function startAutoClick() {
                    if (nextButton && !autoClickInterval) {
                        autoClickInterval = setInterval(() => {
                            nextButton.click();
                        }, 3000); // 3000ms = 3 seconds
                    }
                }

                // Function to stop the automatic clicking
                function stopAutoClick() {
                    if (autoClickInterval) {
                        clearInterval(autoClickInterval);
                        autoClickInterval = null;
                    }
                }

                // Observer to detect if the #partner section is in view
                const observer = new IntersectionObserver((entries) => {
                    entries.forEach((entry) => {
                        if (entry.isIntersecting) {
                            startAutoClick(); // Start auto-clicking when #partner is in view
                        } else {
                            stopAutoClick(); // Stop auto-clicking when #partner is out of view
                        }
                    });
                });

                // Observe the #partner section
                if (partnerSection) {
                    observer.observe(partnerSection);
                }

                // Stop automatic clicking on mouseover and resume on mouseout
                if (nextButton) {
                    nextButton.addEventListener('mouseover', stopAutoClick);
                    nextButton.addEventListener('mouseout', startAutoClick);
                } else {
                    console.error("Button with class 'owl-next' not found in 'custom-css' container.");
                }
            });
        </script>



